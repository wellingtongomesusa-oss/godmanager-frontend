  var currentLang = 'en';
  try {
    var s = localStorage.getItem('gm_lang');
    if (s === 'en' || s === 'pt' || s === 'es') currentLang = s;
  } catch (e) {}

  function t(key) {
    var L = i18n[currentLang];
    if (L && L[key] !== undefined) return L[key];
    if (i18n.en[key] !== undefined) return i18n.en[key];
    return key;
  }

  function setHtmlLang() {
    var h = document.documentElement;
    if (currentLang === 'pt') h.setAttribute('lang', 'pt-BR');
    else if (currentLang === 'es') h.setAttribute('lang', 'es');
    else h.setAttribute('lang', 'en');
  }

  function applyTranslations() {
    setHtmlLang();
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (key) el.innerHTML = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (key) el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-title');
      if (key) el.title = t(key);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-aria');
      if (key) el.setAttribute('aria-label', t(key));
    });
    var lb = document.getElementById('lang-current');
    if (lb) lb.textContent = currentLang.toUpperCase();
  }

  function setLanguage(lang) {
    if (lang !== 'en' && lang !== 'pt' && lang !== 'es') lang = 'en';
    currentLang = lang;
    try {
      localStorage.setItem('gm_lang', currentLang);
    } catch (e) {}
    applyTranslations();
    if (typeof window.gmAfterLangChange === 'function') window.gmAfterLangChange();
  }

  function toggleLangMenu() {
    var m = document.getElementById('lang-menu');
    var b = document.getElementById('lang-current');
    if (!m || !b) return;
    var open = m.style.display !== 'block';
    m.style.display = open ? 'block' : 'none';
    b.setAttribute('aria-expanded', open ? 'true' : 'false');
  }

  document.addEventListener('click', function (e) {
    if (!e.target.closest || !e.target.closest('.tb-lang-wrap')) {
      var m = document.getElementById('lang-menu');
      var b = document.getElementById('lang-current');
      if (m) m.style.display = 'none';
      if (b) b.setAttribute('aria-expanded', 'false');
    }
  });

  window.GM_I18N = i18n;
  window.t = t;
  window.setLanguage = setLanguage;
  window.applyTranslations = applyTranslations;
  window.toggleLangMenu = toggleLangMenu;
  window.getGmLang = function () {
    return currentLang;
  };
  window.setGmLangInternal = function (l) {
    currentLang = l;
  };

